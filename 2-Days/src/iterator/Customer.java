package iterator;

public class Customer {
	
	private String mail;
	private int tc;

	
	public Customer(String mail, int tc) {
		super();
		this.mail = mail;
		this.tc = tc;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public int getTc() {
		return tc;
	}
	public void setTc(int tc) {
		this.tc = tc;
	}
	
	

}
